function s = s_2(x,y)

c_11 = 11.8*(1-x).*y + 8.329*x.*y + 14.12*(1-x).*(1-y) + 10.22*x.*(1-y);
c_11 = 1e11 * c_11 * 1e-1;

c_12 = 5.38*(1-x).*y + 4.526*x.*y + 6.253*(1-x).*(1-y) + 5.76*x.*(1-y);
c_12 = 1e11 * c_12 * 1e-1;

b = -1.7*(1-x).*y - 1.8*x.*y - 1.5*(1-x).*(1-y) - 2.0*x.*(1-y);
b = b * 1.6e-19;

s = - b .* (  1  +  2 * c_12 ./ c_11  ) .*  strain(x,y);